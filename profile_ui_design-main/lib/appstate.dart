class AppState extends ChangeNotifier {

  final Profiles profiles = Profiles();
  var firstName = (Profiles().firstNames..shuffle()).first;
  var lastName = (Profiles().lastNames..shuffle()).first;
  var age = (Profiles().ages..shuffle()).first;
  var description = (Profiles().descriptions..shuffle()).first;
  var image = (Profiles().pictures..shuffle()).first;

  var info = "";

  var likes = [];

  AppState() {
    generateNewProfile();
  }

  void generateNewProfile() {
    firstName = (profiles.firstNames..shuffle()).first;
    lastName = (profiles.lastNames..shuffle()).first;
    age = (profiles.ages..shuffle()).first;
    description = (profiles.descriptions..shuffle()).first;
    image = (profiles.pictures..shuffle()).first;
    notifyListeners();
  }

  void likeProfile() {
    info = "$firstName $lastName";
    likes.add({
      "name": "$firstName $lastName",
      "image": image,
    });
    generateNewProfile();
  }

  void dislikeProfile() {
    generateNewProfile();
  }
}


